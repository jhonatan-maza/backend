package com.proyecto.web.model;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Created by jonat on 23/10/2019.
 */
@JsonInclude(JsonInclude.Include.NON_NULL)
public class Proveedor {

    @JsonProperty("idproveedor")
    private Integer idProveedor;

    @JsonProperty("ubigeo")
    private Distrito ubigeo;

    @JsonProperty("nombrecomercial")
    private String nombreComercial;

    @JsonProperty("ruc")
    private String ruc;

    @JsonProperty("contacto1")
    private String contacto1;

    @JsonProperty("contacto2")
    private String contacto2;

    @JsonProperty("telefono1")
    private String telefono1;

    @JsonProperty("telefono2")
    private String telefono2;

    @JsonProperty("correo")
    private String correo;

    @JsonProperty("estado")
    private String estado;

    @JsonProperty("direccion")
    private String direccion;

    @JsonProperty("observaciones")
    private String observaciones;

    public Integer getIdProveedor() {
        return idProveedor;
    }

    public void setIdProveedor(Integer idProveedor) {
        this.idProveedor = idProveedor;
    }

    public Distrito getUbigeo() {
        return ubigeo;
    }

    public void setUbigeo(Distrito ubigeo) {
        this.ubigeo = ubigeo;
    }

    public String getNombreComercial() {
        return nombreComercial;
    }

    public void setNombreComercial(String nombreComercial) {
        this.nombreComercial = nombreComercial;
    }

    public String getRuc() {
        return ruc;
    }

    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    public String getContacto1() {
        return contacto1;
    }

    public void setContacto1(String contacto1) {
        this.contacto1 = contacto1;
    }

    public String getContacto2() {
        return contacto2;
    }

    public void setContacto2(String contacto2) {
        this.contacto2 = contacto2;
    }

    public String getTelefono1() {
        return telefono1;
    }

    public void setTelefono1(String telefono1) {
        this.telefono1 = telefono1;
    }

    public String getTelefono2() {
        return telefono2;
    }

    public void setTelefono2(String telefono2) {
        this.telefono2 = telefono2;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }
}
